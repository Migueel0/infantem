package com.isppG8.infantem.infantem.sueño;

public enum tipoSueño {
    PROFUNDO, // El bebé se mueve un poco, hace gestos o succiona mientras duerme.
    LIGERO, // El bebé tiene un sueño tranquilo, sin movimientos ni expresiones faciales.
    REM, // El bebé hace movimientos rápidos con los ojos, posibles sonrisas o gestos.
    AGITADO // El bebé se mueve más de lo normal, respira irregularmente.

}
